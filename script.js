let shapeType = '';

class Shape {
    constructor(sides, noofSides, noOfEqualSides, radius, diameter, length, width) {
        this.sides = sides;
        this.noOfEqualSides = noOfEqualSides;
        this.diameter = diameter;
        this.length = length;
        this.width = width;
        this.noOfsides = noofSides;
        this.radius = radius;
    }


//First 
    determineShape() {
        if(this.sides === false && this.noOfsides === false && this.noOfEqualSides === false && this.radius === true && this.diameter === true && this.length === false && this.width === false) {
            shapeType = circle;
        }else if(this.sides === true && this.noOfsides === 3 && this.noOfEqualSides === true && this.radius === false && this.diameter === false && this.length === true && this.width === true) {
            //based on the number of equal sides, set as scalene, equilateral or right angled
            shapeType = triangle;
        }else if(this.sides === true && this.noOfsides === true && this.noOfEqualSides === 2 && this.radius === false && this.diameter === false && this.length === true && this.width === true) {
            shapeType = rectangle;
        } else if(this.sides === true && this.noOfsides === true && this.noOfEqualSides === 4 && this.radius === false && this.diameter === false && this.length === true && this.width === true) {
            shapeType = square;
        }else {
            shapeType = 'Shape Not Found!'; 
        }

        return shapeType;

    }





}

